package longnd.com.vn.thesis.data.entity;

public class APoint {
    float x;
    float y;

    public APoint() {

    }

    public APoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
